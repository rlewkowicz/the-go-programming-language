func main() {
	http.HandleFunc("/", func(w http.ResponseWriter, r *http.Request) {
		val, _ := url.ParseQuery(r.URL.RawQuery)
		lissajous(w, val)
	})
	http.HandleFunc("/debug", func(w http.ResponseWriter, r *http.Request) {
		val, _ := url.ParseQuery(r.URL.RawQuery)
		fmt.Fprintf(w, "%v\n", val["scoodle"][0])
	})
	log.Fatal(http.ListenAndServe("localhost:8000", nil))
}

func random(min, max int) uint8 {
	rand.Seed(time.Now().Unix())
	return uint8(rand.Intn(max-min) + min)
}

func lissajous(out io.Writer, val map[string][]string) {
	cycles := 5.0
	res := 0.001
	size := 100
	nframes := 64
	delay := 8
	if val["cycles"] != nil {
		cycles, _ = strconv.ParseFloat(val["cycles"][0], 64)
	}
	if val["res"] != nil {
		res, _ = strconv.ParseFloat(val["res"][0], 64)
	}
	if val["size"] != nil {
		size, _ = strconv.Atoi(val["size"][0])
	}
	if val["nframes"] != nil {
		nframes, _ = strconv.Atoi(val["nframes"][0])
	}
	if val["delay"] != nil {
		delay, _ = strconv.Atoi(val["delay"][0])
	}
	freq := rand.Float64() * 3.0
	anim := gif.GIF{LoopCount: nframes}
	phase := 0.0
	for i := 0; i < nframes; i++ {
		rect := image.Rect(0, 0, 2*size+1, 2*size+1)
		img := image.NewPaletted(rect, pallete)
		for t := 0.0; t < cycles*2*math.Pi; t += res {
			x := math.Sin(t)
			y := math.Sin(t*freq + phase)
			img.SetColorIndex(size+int(x*float64(size)+.5), size+int(y*float64(size)+.5), random(3, 150))
		}
		phase += 0.1
		anim.Delay = append(anim.Delay, delay)
		anim.Image = append(anim.Image, img)
	}
	gif.EncodeAll(out, &anim)
}
